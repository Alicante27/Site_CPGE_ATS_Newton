		<div class="titre2"><h2>Écoles d'ingénieur</h2></div>		
		
		<div class="partie">
		<div class="case_centr" ><div class="shadow" ><div class="content2" ><div class="text2">
			L'ATS Ingénierie Industrielle a pour objectif principal de préparer les élèves à intégrer une <b>école
			d'ingénieur</b> via le <b>concours national ATS</b>. Les principaux débouchés sont:
			<ul>
				<li> Le <a href="https://concours.ensea.fr/portail/ats.html">concours national ATS</a> (une cinquantaine d'écoles d'ingénieurs, et environ 500 places). </li>
				<li> Le  <a href="https://www.centralesupelec.fr/fr/le-cursus-ingenieur?tab=programme-grande-ecole-voies-dadmission">concours CentraleSupélec</a> (20 places en apprentissage). </li>
				<li> Les <a href="ATS_ii_ecole_inge.php">écoles d'ingénieurs "sur dossier"</a>. </li>
				<li> Les Licences et <a href="https://www.onisep.fr/Choisir-mes-etudes/Apres-le-bac/Organisation-des-etudes-superieures/Les-licences-professionnelles">licences Pro</a>.</li>
			</ul>
		</div></div></div></div>
	
		<p>
		Le concours national ATS est réservé aux élèves inscrits en ATS Ingénierie Industrielle.
		Certaines des écoles d'ingénieurs les plus prestigieuses proposent des places au concours ATS.
		Par exemple, lors du concours 2022, il y avait 25 places aux Arts et Métiers, 6 places à Centrale Lille, 5 places à Télécom Paris.
		En 2021, exactement 328 élèves (pour 1040 inscrits) ont intégré une école du concours ATS. <br>
		Les élèves qui n’intègrent pas d'école au concours ATS intègrent généralement des écoles sur dossier.
		La quasi-totalité des élèves d'ATS continuent leurs études en école d'ingénieur en formation initiale ou en apprentissage.
		</p></div>
		
